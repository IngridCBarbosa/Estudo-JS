const raio = 5.6
const area = Math.PI * Math.pow(raio,2)

/* Math.PI é o pi

    Math.pow é a potência no exemplo acima é raio elevado ao quadrado
*/

console.log(area) // sem limitação de casas decimais 
console.log(area.toFixed(2)) // com limitação de casa decimais
